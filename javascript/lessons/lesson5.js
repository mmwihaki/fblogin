// switch statements 

let color = 'GREEN';

switch (color) {
    case 'green':
        console.log('GO!');
        break;
    case 'yellow':
        console.log('GET READY');
        break;
    case 'red':
        console.log('STOP');
        break;
    default:
        console.log('ERROR! INVALID INPUT');
        break;
}